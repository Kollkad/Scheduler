    def create_custom_config(
            self,
            columns: List[Tuple[str, str, Optional[str]]]
    ) -> List[Dict]:
        """
        –°–æ–∑–¥–∞–µ—Ç –ø–æ–ª—å–∑–æ–≤–∞—Ç–µ–ª—å—Å–∫—É—é –∫–æ–Ω—Ñ–∏–≥—É—Ä–∞—Ü–∏—é –æ–±–µ–∑–ª–∏—á–∏–≤–∞–Ω–∏—è.

        Args:
            columns: –°–ø–∏—Å–æ–∫ –∫–æ—Ä—Ç–µ–∂–µ–π (–∏–º—è_–∫–æ–ª–æ–Ω–∫–∏, —Ç–∏–ø_–∑–∞–º–µ–Ω—ã, —Ç–µ–∫—Å—Ç_–∑–∞–º–µ–Ω—ã).
                    –ï—Å–ª–∏ —Ç–µ–∫—Å—Ç_–∑–∞–º–µ–Ω—ã —Ä–∞–≤–µ–Ω None, –∏—Å–ø–æ–ª—å–∑—É–µ—Ç—Å—è –∞–≤—Ç–æ–º–∞—Ç–∏—á–µ—Å–∫–∏
                    —Å–≥–µ–Ω–µ—Ä–∏—Ä–æ–≤–∞–Ω–Ω—ã–π —Ç–µ–∫—Å—Ç –Ω–∞ –æ—Å–Ω–æ–≤–µ –∏–º–µ–Ω–∏ –∫–æ–ª–æ–Ω–∫–∏.

        Returns:
            List[Dict]: –ö–æ–Ω—Ñ–∏–≥—É—Ä–∞—Ü–∏—è –≤ —Ñ–æ—Ä–º–∞—Ç–µ, —Å–æ–≤–º–µ—Å—Ç–∏–º–æ–º —Å anonymize_dataframe.

        Raises:
            ValueError: –ï—Å–ª–∏ –ø–µ—Ä–µ–¥–∞–Ω –Ω–µ–∫–æ—Ä—Ä–µ–∫—Ç–Ω—ã–π —Ç–∏–ø –∑–∞–º–µ–Ω—ã.
        """
        config = []

        # –û–±—Ä–∞–±–æ—Ç–∫–∞ –∫–∞–∂–¥–æ–π –∫–æ–ª–æ–Ω–∫–∏ –∏–∑ –≤—Ö–æ–¥–Ω–æ–≥–æ —Å–ø–∏—Å–∫–∞
        for col_name, replace_type, replace_text in columns:
            if replace_text is None:
                # –ì–µ–Ω–µ—Ä–∞—Ü–∏—è —Ç–µ–∫—Å—Ç–∞ –∑–∞–º–µ–Ω—ã –ø—Ä–æ–∏—Å—Ö–æ–¥–∏—Ç –ø—É—Ç–µ–º —Ñ–æ—Ä–º–∞—Ç–∏—Ä–æ–≤–∞–Ω–∏—è –∏–º–µ–Ω–∏ –∫–æ–ª–æ–Ω–∫–∏
                replace_text = col_name.replace('_', ' ').title()

            config.append({
                'column': col_name,
                'type': replace_type,
                'replacement': replace_text
            })

        return config